<?php

namespace App\Imports;

use App\Models\Blog;
use Maatwebsite\Excel\Concerns\ToModel;

class BlogsImport implements ToModel
{
    /**
     * @param array $row
     *
     * @return \Illuminate\Database\Eloquent\Model|null
     */
    public function model(array $row)
    {
        return new Blog([
            'url' => $row[1],
            'title' => $row[2],
            'content' => $row[3],
            'meta_description' => $row[4],
        ]);
    }
}
